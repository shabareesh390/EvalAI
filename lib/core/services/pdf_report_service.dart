                border: pw.Border.all(color: PdfColors.grey300),
                borderRadius: const pw.BorderRadius.all(pw.Radius.circular(8)),
              ),
              child: pw.Row(
                mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
                children: [
                  pw.Column(
                    crossAxisAlignment: pw.CrossAxisAlignment.start,
                    children: [
                      pw.Text('Student: ${evaluation.studentName}', style: pw.TextStyle(fontWeight: pw.FontWeight.bold)),
                      pw.Text('Roll No: ${evaluation.studentRollNumber}'),
                    ],
                  ),
                  pw.Column(
                    crossAxisAlignment: pw.CrossAxisAlignment.end,
                    children: [
                      pw.Text('Exam: ${evaluation.examName}', style: pw.TextStyle(fontWeight: pw.FontWeight.bold)),
                      if (subject.isNotEmpty) pw.Text('Subject: $subject'),
                    ],
                  ),
                ],
              ),
            ),
            pw.SizedBox(height: 24),

            // ─── AI Overall Feedback ───

                  ),
                  child: pw.Text(
                    '${evaluation.totalMarksAwarded} / ${evaluation.totalMarks}',
                    style: pw.TextStyle(
                      fontSize: 18,
                      fontWeight: pw.FontWeight.bold,
                      color: evaluation.isPassed ? PdfColors.green800 : PdfColors.red800,
                    ),
                  ),
                ),
              ],
            ),
            pw.SizedBox(height: 24),

            // ─── Student & Exam Details ───
